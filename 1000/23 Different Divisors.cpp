ll findPrime(ll n) {
  for (ll i = n;; i++) {
    bool prime = true;
    for (ll j = 2; j*j <= i; j++) {
      if (i % j == 0) {
        prime = false;
        break;
      }
    }
    if (prime) return i;
  }
}

void solve() {
  ll d; cin >> d;

  ll a = findPrime(d+1);
  ll b = findPrime(d+a);

  cout << min(a*a*a, a*b) << endl;
}

